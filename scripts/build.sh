#!/bin/bash
# LTOG build driver. Run inside an MSYS2 MINGW64 shell after scripts/setup.sh:
#   ./scripts/build.sh           # build everything and stage dist/
#   ./scripts/build.sh make      # just compile the LTFS tree
#   ./scripts/build.sh filedebug # just the file-emulator backend
#   ./scripts/build.sh dist      # just (re)stage dist/
#   ./scripts/build.sh clean
set -uo pipefail

ROOT="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
SRC="$ROOT/third_party/ltfs/ltfs"
DIST="${DIST:-$ROOT/dist}"   # override with DIST=... to stage elsewhere
export PKG_CONFIG_PATH="$ROOT/pc:${PKG_CONFIG_PATH:-}"

do_make() {
    cd "$SRC"
    make -j"$(nproc)"
}

do_filedebug() {
    # The file-emulator tape backend (test the filesystem with no tape
    # hardware). Not part of HP's Windows SUBDIRS, so it is built directly.
    cd "$SRC/messages"
    ./make_message_src.sh driver_generic_file_dat.o
    cd "$SRC/src/tape_drivers/generic/file"
    x86_64-w64-mingw32-gcc -shared -o libdriver-file.dll filedebug_tc.c \
        -DHAVE_CONFIG_H -I"$SRC" -I"$SRC/src" \
        -D_GNU_SOURCE -DGENERIC_OEM_BUILD -Dmingw_PLATFORM=1 -DHP_mingw_BUILD=1 \
        -DHPE_mingw_BUILD=1 -D_FILE_OFFSET_BITS=64 \
        -DWINVER=0x0601 -D_WIN32_WINNT=0x0601 \
        $(pkg-config --cflags fuse libxml-2.0 icu-uc) \
        -L"$SRC/src/libltfs/.libs" -lltfs \
        -L"$SRC/messages" -ldriver_generic_file \
        $(pkg-config --libs fuse) -lws2_32 \
        -Wl,--out-implib,libdriver-file.dll.a
    echo "filedebug backend built"
}

do_dist() {
    mkdir -p "$DIST"
    echo "==> Staging executables and plugins"
    cp "$SRC/src/.libs/ltfs.exe" \
       "$SRC/src/utils/.libs/mkltfs.exe" \
       "$SRC/src/utils/.libs/ltfsck.exe" \
       "$SRC/src/utils/.libs/unltfs.exe" \
       "$SRC/src/libltfs/.libs/libltfs.dll" \
       "$SRC/src/tape_drivers/windows/ltotape/.libs/libdriver-ltotape-win.dll" \
       "$SRC/src/iosched/.libs/libiosched-unified.dll" \
       "$SRC/src/iosched/.libs/libiosched-fcfs.dll" \
       "$SRC/src/kmi/.libs/libkmi-flatfile.dll" \
       "$SRC/src/kmi/.libs/libkmi-simple.dll" \
       "$DIST/"
    cp "$SRC/src/tape_drivers/generic/file/libdriver-file.dll" "$DIST/" 2>/dev/null \
        || echo "    (filedebug backend not built - emulator unavailable)"
    cp "$SRC"/messages/lib*.dll "$DIST/"          # ICU message catalogs
    cp "$ROOT/build/wfsp/bin/winfsp-x64.dll" "$DIST/"

    echo "==> Copying MinGW runtime DLLs"
    cd "$DIST"
    for _ in 1 2 3; do  # iterate: copied DLLs may pull more deps
        ldd ./*.exe ./*.dll 2>/dev/null \
            | awk '/mingw64/ {print $3}' | sort -u \
            | while read -r dep; do cp -n "$dep" . 2>/dev/null || true; done
    done

    echo "==> Generating ltfs.conf"
    DISTW="$(cygpath -m "$DIST")"
    cat > "$DIST/ltfs.conf" <<EOF
# Generated by scripts/build.sh - plugin registry for this dist directory
plugin driver ltotape_win $DISTW/libdriver-ltotape-win.dll
plugin driver file $DISTW/libdriver-file.dll
plugin iosched unified $DISTW/libiosched-unified.dll
plugin iosched fcfs $DISTW/libiosched-fcfs.dll
plugin kmi flatfile $DISTW/libkmi-flatfile.dll
plugin kmi simple $DISTW/libkmi-simple.dll
default driver ltotape_win
default iosched unified
default kmi none
EOF

    echo "==> Staging license + third-party notices"
    cp "$ROOT/LICENSE" "$ROOT/THIRD-PARTY-NOTICES.md" "$DIST/"
    cp -r "$ROOT/licenses" "$DIST/"

    echo "==> dist ready: $DISTW  ($(ls -1 "$DIST" | wc -l) files)"
    echo "    (build the GUI with: cd gui && dotnet build -c Release -p:Platform=x64,"
    echo "     then copy its output into $DISTW/gui)"
}

cmd=${1:-all}
case "$cmd" in
make)      do_make ;;
filedebug) do_filedebug ;;
dist)      do_dist ;;
all)       do_make && do_filedebug && do_dist ;;
clean)     cd "$SRC" && make clean ;;
*)         echo "usage: build.sh [all|make|filedebug|dist|clean]" >&2; exit 2 ;;
esac
